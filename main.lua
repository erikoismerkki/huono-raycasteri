function cast()
  rx=px
  ry=py
  rdist=0
  
  
  ra=pa
  
  for angle=0,60 do
    ra=pa+math.rad(angle)-math.rad(30)
    rx=px
    ry=py
    rdist=0
    for i=0,2^10 do
      rx=rx+math.cos(ra)
      ry=ry+math.sin(ra)
      rmx=rx/cellsize
      rmy=ry/cellsize
      
      if math.abs(math.floor(rx/cellsize+0.5)-rx/cellsize)<math.abs(math.floor(ry/cellsize+0.5)-ry/cellsize)  then
        normal=0.95
      else
        normal=1
      end
      
      if map[math.floor(rmy)*mapw+math.floor(rmx)+1] ==2 then
        color=2
      elseif map[math.floor(rmy)*mapw+math.floor(rmx)+1] ==3 then
        color="E"
      else
        color=1
      end
      
      if map[math.floor(rmy)*mapw+math.floor(rmx)+1] >=1 then
        if angle==30 then walkidi=rdist end
        break 
      end
      if i==2^10 then rdist=10000000 end
      
      rdist=rdist+1
      --love.graphics.points(rx,ry)
    end
    
    lineh=cellsize*600/rdist
    
    --[[if rmx-math.floor(rmx+0.5) < rmy-math.floor(rmy+0.5) then
      love.graphics.setColor(1,0,0,1)
    else
      love.graphics.setColor(0,1,0,1)
    end]]
    
    
    
    if color==1 then
      love.graphics.setColor(math.log(math.log(math.log(lineh)))*normal,0,0,1)
