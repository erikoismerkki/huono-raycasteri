function love.update(dt)
  d=0
  if love.keyboard.isDown("w") then
    if walkidi>20 then d=125*dt end
  elseif love.keyboard.isDown("s") then
    d=d-(75*dt)
    dx=math.cos(pa)*d
    dy=math.sin(pa)*d
    px=px+dx*3
    py=py+dy*3
    pmx=math.floor(px/cellsize)
    pmy=math.floor(py/cellsize)
    if map[pmy * mapw + pmx+1] >0 then
      d=0
    end
    px=px-dx*3
    py=py-dy*3
  elseif love.keyboard.isDown("a") then
    px=px+math.cos(pa-math.rad(90))*dt*75*5
    py=py+math.sin(pa-math.rad(90))*dt*75*5
    pmx=math.floor(px/cellsize)
    pmy=math.floor(py/cellsize)
    if map[pmy * mapw + pmx+1] >0 then
      px=px-math.cos(pa-math.rad(90))*dt*75*1
      py=py-math.sin(pa-math.rad(90))*dt*75*1
    end
    px=px-math.cos(pa-math.rad(90))*dt*75*4
    py=py-math.sin(pa-math.rad(90))*dt*75*4
  elseif love.keyboard.isDown("d") then
    px=px-math.cos(pa-math.rad(90))*dt*75*5
    py=py-math.sin(pa-math.rad(90))*dt*75*5
    pmx=math.floor(px/cellsize)
    pmy=math.floor(py/cellsize)
    if map[pmy * mapw + pmx+1] >0 then
      px=px-math.cos(pa+math.rad(90))*dt*75*1
      py=py-math.sin(pa+math.rad(90))*dt*75*1
    end
    px=px+math.cos(pa-math.rad(90))*dt*75*4
    py=py+math.sin(pa-math.rad(90))*dt*75*4
  end
  
  if love.keyboard.isDown("left") then pa=pa-(2*dt) end
  if pa<0 then pa=pa+math.rad(360) end
  if love.keyboard.isDown("right") then pa=pa+(2*dt) end
  if pa>math.rad(360) then pa=pa-math.rad(360) end
  dx=math.cos(pa)*d
  dy=math.sin(pa)*d
  px=px+dx
  py=py+dy
  pmx=px/cellsize
  pmy=py/cellsize
  
  -------------------------------------------------fps kalkulaatori
  fps=1/dt
  table.insert(fpstable,fps)
  for i,v in ipairs(fpstable) do
    smoothfps=smoothfps+fpstable[i]
  end
  smoothfps=smoothfps/#fpstable
  if #fpstable>120 then table.remove(fpstable,1) end
  -------------------------------------------------
  
end

